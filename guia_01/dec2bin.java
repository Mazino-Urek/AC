public class dec2bin {

public static String decbin (int x ){

	return Integer.toBinaryString(x);

}

public static void main(String[] args){

	System.out.println(decbin(23));
	System.out.println(decbin(54));
	System.out.println(decbin(731));
	System.out.println(decbin(132));
	System.out.println(decbin(365));





}


}
